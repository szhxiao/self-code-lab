public class ConstantTest {
    public static void main(String[] args) {
        final double PI = 3.14;
        double r = 4;
        double area = PI * r * r;
        double circle = 2 * PI * r;

        System.out.println("area = " + area);;
        System.out.println("circle = " + circle);
        System.out.println("circle = " + circle);

    }
}