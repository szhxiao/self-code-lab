/**
 * 测试Math类
 */

public class MathTest {

    /**
     * main()
     * 
     * @param args array of string arguments
     */
    public static void main(String[] args) {
        System.out.println("ceil(3.2) = " + Math.ceil(3.2));
        System.out.println("floor(3.2) = " + Math.floor(3.2));
        System.out.println("round(3.2) = " + Math.round(3.2));
        System.out.println("round(3.8) = " + Math.round(3.8));
        System.out.println("abs(-45) = " + Math.abs(-45));
        System.out.println("sqrt(64) = " + Math.sqrt(64));
        System.out.println("pow(5, 2) = " + Math.pow(5, 2));
        System.out.println("pow(2, 5) = " + Math.pow(2, 5));
        System.out.println("PI = " + Math.PI);
        System.out.println("e = " + Math.E);
        System.out.println("random() = " + Math.random());
    }
}

